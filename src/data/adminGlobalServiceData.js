export function computeSummaryCounts(calls) {
  const open = calls.filter(isOpenGlobalCall);
  const overdueAging = calls.filter(
    (c) => c.isOverdue || (c.isAging && isOpenGlobalCall(c))
  );
  return {
    open: open.length,
    pending_dispatch: calls.filter((c) => c.statusKey === 'pending_dispatch').length,
    in_progress: calls.filter((c) => c.statusKey === 'in_progress').length,
    waiting_parts: calls.filter((c) => c.statusKey === 'waiting_parts').length,
    overdue_aging: overdueAging.length,
    resolved_week: calls.filter((c) => c.statusKey === 'resolved' && resolvedThisWeek(c.resolvedAt)).length,
  };
}

/** Top 3 customers by open call count */
export function topCustomersByOpenCalls(calls) {
  const counts = {};
  calls.filter(isOpenGlobalCall).forEach((c) => {
    counts[c.customerName] = (counts[c.customerName] || 0) + 1;
  });
  return Object.entries(counts)
    .sort((a, b) => b[1] - a[1])
    .slice(0, 3)
    .map(([name, count]) => ({ name, count }));
}
