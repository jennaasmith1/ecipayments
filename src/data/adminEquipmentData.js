export function sortEquipment(list, sortKey, sortDir) {
  const mult = sortDir === 'asc' ? 1 : -1;
  return [...list].sort((a, b) => {
    if (sortKey === 'customer') return mult * a.customerName.localeCompare(b.customerName);
    if (sortKey === 'equipmentNo') return mult * a.equipmentNo.localeCompare(b.equipmentNo);
    if (sortKey === 'location') return mult * a.label.localeCompare(b.label);
    if (sortKey === 'status') return mult * a.reportingStatus.localeCompare(b.reportingStatus);
    if (sortKey === 'toner') {
      const order = { critical: 0, low: 1, good: 2 };
      return mult * (order[a.tonerHealth] - order[b.tonerHealth]);
    }
    if (sortKey === 'lastMeter') {
      return mult * (a.lastMeterRead < b.lastMeterRead ? -1 : a.lastMeterRead > b.lastMeterRead ? 1 : 0);
    }
    return mult * a.displayName.localeCompare(b.displayName);
  });
}
