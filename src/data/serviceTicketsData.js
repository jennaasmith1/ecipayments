export const TERMINAL_SERVICE_STATUSES = new Set(['completed', 'closed', 'resolved']);

export function isOpenServiceTicket(t) {
  return !TERMINAL_SERVICE_STATUSES.has(t.status);
}

export function contractOnTicketIsCovered(c) {
  return c !== 'none';
}

/** @param {typeof initialServiceTickets[0]} ticket @param {Record<string, object>} fleetById */
export function resolveTicketEquipment(ticket, fleetById) {
  return fleetById[ticket.equipmentId] ?? null;
}

