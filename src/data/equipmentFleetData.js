export function getFleetSummary(fleet = fleetEquipment) {
  const tonerIssues = fleet.filter((d) => d.tonerHealth === 'low' || d.tonerHealth === 'critical').length;
  const healthy = fleet.filter(
    (d) => d.reportingStatus === 'online' && !d.meterDue && d.tonerHealth === 'good'
  ).length;
  return {
    total: fleet.length,
    online: fleet.filter((d) => d.reportingStatus === 'online').length,
    offline: fleet.filter((d) => d.reportingStatus === 'offline').length,
    healthy,
    meterDue: fleet.filter((d) => d.meterDue).length,
    tonerIssues,
  };
}

/** Slim shape for Service.jsx, CustomerDetail, HomeDashboard */
export function toLegacyEquipment(d) {
  const name = d.nickname ? `${d.nickname} — ${d.model}` : d.displayName;
  const hasAlert =
    d.reportingStatus === 'offline' || d.meterDue || d.tonerHealth !== 'good';
  return {
    id: d.id,
    name,
    model: d.model,
    serialNumber: d.serialNumber,
    location: d.label,
    status: d.reportingStatus === 'offline' ? 'offline' : 'active',
    needsAttention: hasAlert,
    lastMeterRead: d.lastMeterRead,
    lastMeterValue: d.lastMeterValue,
    meterType: d.meterType,
  };
}

export const legacyEquipmentList = fleetEquipment.map(toLegacyEquipment);
